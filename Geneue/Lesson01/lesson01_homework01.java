
public class lesson01_homework01 {

	public static void main(String[] args) {
		
		double radius = 1.6;
		
		double area = 3.1416 * radius * radius;
		
		double circumference = 2 * 3.1416 * radius;
		
		System.out.println("If the radius of a circle is " + radius + " then it's area = " + area + " and circumference = " +
		circumference + '.');

	}
}
